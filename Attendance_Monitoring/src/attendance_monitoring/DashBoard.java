    public void getData(){
    
       int count = 0;
       try {
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/monitoring_attendance", "root", "");
           Statement stmt = con.createStatement();
         
           ResultSet datas = stmt.executeQuery("select * from project");
           DefaultTableModel model = (DefaultTableModel) projectTable.getModel();
           while (datas.next()) {
               count = 1;
               model.addRow(new Object[]{datas.getString("project_id"),datas.getString("project_title"), datas.getString("department"), datas.getString("status")});
           }
           if (count == 0) {
               JOptionPane.showMessageDialog(null,"No data found!.","Alert",JOptionPane.WARNING_MESSAGE);  
           }
           
//           count number of employee
           datas = stmt.executeQuery("select count(*) from employeetable");
           datas.next();
           int employee = datas.getInt(1);
           numOfEmployee.setText(String.valueOf(employee));
          
//       count number of projects
          datas = stmt.executeQuery("select count(*) from project");
          datas.next();
          int project = datas.getInt(1);
          numOfProjects.setText(String.valueOf(project));
          
          //       count number of projects
          datas = stmt.executeQuery("select count(*) from project");
          datas.next();
          int title = datas.getInt(1);
          titleLabel.setText(String.valueOf(title));
          
           con.close();
       } catch (Exception e) {
//          System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null,e.getMessage(),"Alert",JOptionPane.WARNING_MESSAGE);  
       }
    }
    

